function reproducirAudio1() {
  var audio1 = document.getElementById("audio1");
  if (audio1.paused) {
    audio1.play();
    document.getElementById("boton1").innerHTML = "Pausar lectura";
  } else {
    audio1.pause();
    document.getElementById("boton1").innerHTML = "Escuchar lectura";
  }
}

